package StudentManagement;

public class name {
	int No;
	String Name;
	
	public name() {}
	public name(int No, String Name) {
		super();
		this.No = No;
		this.Name = Name;
	}
	public int getNo() {
		return No;
	}
	public void setNo(int no) {
		No = no;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
}
